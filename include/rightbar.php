                 <p class="first_pera">The Ministry has taken up detailed review of NHs network with a view to develop the road connectivity to Border areas, development of Coastal roads including road connectivity for Non-Major ports, improvement in the efficiency of National Corridors, development of Economic Corridors, Inter Corridors and Feeder Routes along with integration with Sagarmala, etc., under Bharatmala Pariyojana. The Bharatmala Pariyojana envisages development of about 26,000 km length of Economic Corridors, which along with Golden Quadrilateral (GQ) and North-South and East-West (NS-EW) Corridors are expected to carry majority of the Freight Traffic on roads. Further, about 8,000 km of Inter Corridors and about 7,500 km of Feeder Routes have been identified for improving effectiveness of Economic Corridors, GQ and NS-EW Corridors. The programme envisages development of Ring Roads / bypasses and elevated corridors to decongest the traffic passing through cities and enhance logistic efficiency; 28 cities have been identified for Ring Roads; 125 choke points and 66 congestion points have been identified for their improvements. Further, in order to reduce congestion on proposed Corridors, enhance logistic efficiency and reduce logistics costs of freight movements, 35 locations have been identified for development of Multimodal Logistics Parks.</p>
                 <p class="pera">The Public Investment Board (PIB) has recommended the proposal during its meeting held on 16.06.2017. Cabinet Committee on Economic Affairs (CCEA) has approved the Bharatmala Pariyojana Phase-I. The minutes of the meeting were circulated vide Cabinet Secretariat’s letter dated 26.10.2017.</p>
                 <p  class="pera">The Bharatmala (approved for estimated cost of Rs. 6,92,324 crore including other ongoing schemes) is to be funded from Cess (Rs.  2,37,024 crore) collected form Petrol & Diesel (as per Central Road & Infrastructure Fund Act, 2000, erstwhile CRF Act, 2000), amount collected from toll (Rs. 46,048 crore) apart from additional budgetary support (Rs. 59,973 crore), Expected monetisation of NHs through TOT (Toll-Operate-Transfer) (Rs. 34,000 crore), Internal & Extra Budgetary Resources (IEBR)  (Rs. 2,09,279 crore ) and Private Sector Investment (Rs. 1,06,000 crore) as per Financing Plan upto 2021-22.</p>
